  <h2>Why another ad-blocker?</h2>

  <p>
    Most ad-blockers approach the problem of ads from a *very* different angle than Retheme.
    Simply put, they block the sources of known offenders (ad servers, malicious domains, etc), preventing them from being served to the page.
  </p>

  <h3>These are awesome at:</h3>
  <ul>
    <li>Drastically cutting down on the crap that gets downloaded - speeding up your experience.</li>
    <li>Blocking even video ads without blocking the main video itself.  Handy!</li>
    <li>Protecting you from malicious sites.  That's kind of a big deal.</li>
  </ul>

  <h3>But not-so-awesome at:</h3>
  <ul>
    <li>Fighting the infinite resources of the ad agencies.  For every IP that gets blocked on an ad-blocker, they can spin up a hundred in its place.  It's a losing battle.</li>
    <li>Cleaning up specific sites.  They approach things in a global manner, targeting the sources of the ads, but not the regions of the ads themselves (within the page).  This may leave unsightly gaps and empty boxes where ads once were.</li>
    <li>Removing bespoke or unknown ad sources.</li>
    <li>Removing distracting elements on the page that *aren't* ads.  For example, huge newsletter signup forms, or alerts that keep popping up.</li>
  </ul>

  <h3>What does Retheme do differently?</h3>
  <ul>
    <li>Serves a generic global theme to *ALL* sites, removing the common regions they use to display ads.  It's not great, but it may help a bit.</li>
    <li>Serves highly customized per-site themes, if available.  These are hand-written to clean up a specific site and improve the experience.
      I've mostly targeted high-traffic news and content sites (as we spend a fair big of time clicking into links like that).</li>
    <li>Targets the site itself (code/styles), rather than the endless pool of ad servers.
      Ad agencies may move faster than ad-blockers, but a typical news outlet engineering team can't combat my themes faster than I can make them.
    </li>
    <li>Ads still get served (although hidden), allowing site owners to keep collecting "impression" revenue - this wasn't my goal, but it's a bonus.</li>
    <li>Shouldn't trigger the "remove your ad-blocker" messages from sites.</li>
  </ul>

  <h3>Should I keep using my other ad-blocker?</h3>
  <p>
    Yep! It's kind of like having a seatbelt AND an airbag.  They solve different things, so you probably want both in a crash!
  </p>
